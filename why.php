    <div class="pagehead">Why a canard?</div>
    <p>I have wanted to build an airplane for a while... pretty much since I was still in diapers. Did I want a jet, a seaplane, a powered parachute? No! I figure if I am going to be investing 2000+ hours and 50K+ in a project, I better be able to use it...
      as often as I want to. I looked into all of the kit planes, stupid expensive prices that I would have had to come up with up front. That's not how I roll... I don't ever see a time in my life that I will have 100,000 dollars in the same place at
      the same time so a plans built plane is the answer.</p>
    <p> So why a <a href="http://www.cozyaircraft.com" target="_blank">Cozy Mark IV</a> or <a href="http://www.aerocanard.com" target="_blank">Aerocanard</a>? Why not?</p>
    <p>Have you seen how Star Wars these things look? I am a geek... plain and simple, that is the reason. A <a href="https://www.vansaircraft.com/" target="_blank">Vans</a> is cool but it looks like every other spam can on the flightline,
      the <a href="http://cubcrafters.com/carboncub" target="_blank">Carbon Cub</a>? Super cool idea, again, 100K+ and a cruise speed of 60 kts. No thanks... plus it looks like everything else on the apron. A Cozy has an unmatched presence, besides the fact it was designed by one of my heroes, Burt Rutan, it looks
      like it should be created about 150 years from now. I can dig that, I bought the plans, designed a few ideas, thought a lot, talked to my wife, designed some more... and here we are.</p>
    <img src="img/cozy172.png">

    <h2>What are my goals?</h2>
    <blockquote>
      <li>Hold 4 adults and baggage (duffle bag/person)</li>
      <li>Cruise 200+ kts TAS</li>
      <li>Glass cockpit with total redundancy (2 axis IFR capable)</li>
      <li>Classy, Modern and Exciting</li>
      <li>Able to go across the country (BRL/EOK-0E0) at a moments notice</li>
    </blockquote>

    <h2>Four Seats</h2>
    <p>My gorgeous wife and I <s>are in the beginning stages of starting a family.</s> have two kids (Wow! time flies) I know I reiterate this a lot, but if I am going to make this substantial investment in a project, I better be able to use it. I want to
      spend time with my family and a 2 seater plane is not the way to do it. Another win for the Cozy. I have a few modifications in the works to hopefully make the aircraft a little bit more kids friendly, but I think the Cozy is the best aircraft for
      my needs..</p>

    <h2>IFR and glass?</h2>
    <p>I am a commercial pilot with a instrument rating. It is expensive to rent an airplane all the time to stay current, go visit family, etc. A non-IFR panel will get me out of my home airport and back in in VFR conditions, I may need to go half way across
      the US in the winter or in a dust storm or whatever, I worked for the rating, I might as well put it to use.</p>
    <p>As far as glass, refer back to my geek comment, I am a sucker for technology, when I can get a totally redundant glass cockpit set up for around the same price as a name brand steam gauge setup... I would be stupid not to... plus it is coooool!</p>
    <h2>When will I be done?</h2>
    <p>Honestly? I have no idea, I figure if I put 75,000 dollars in this project and work on it for around 3000 hours, I am spending $25 per hour. There are not very many hobbies you can spend that small amount of money per hour and have an airplane like
      this to show for it. On top of that, I am learning time management, and composite and project management skills. I look at this journey very selfishly, I have wanted this since I was a little boy, I have the means to do it, I am going to appease
      the little kid inside of me :)</p>
    <p> Since I took the plans built approach, I can screw up on whatever I need to as many times as I need to to get the best airplane possible, an ILS approach down to minimums at night is not the place I want to be worrying about whether I bit off more
      than I can chew when I started this project. I plan on visitng a lot of other builders, hitting up a lot of the fly-ins and bending peoples ear on every part of my airplane. Do you hear that, my airplane... it sounds soooo unreal! I have a passion
      for aviation and design, this project neatly intertwines the two of my loves.</p>
